//Description: Calculate the sum of two integers a and b, but you are not allowed to use the operator + and -.
//Source: https://leetcode.com/problems/sum-of-two-integers/

/*
Use method sum() of class Integer to solve. 
*/ 

class Solution {
    public int getSum(int a, int b) {
        int result = 0;
        result = Integer.sum(a,b);
        return result; 
    }
}
